public class NumericPattern7 {
  public static void main(String[] args) {
    {
      int i, j, count;
      count = 1;
      for (i = 1; i <= 5; i++) {
        for (j = 1; j <= 3; j++) {
          System.out.print(" " + count);
          count++;
        }
        System.out.println();
      }
    }
  }

}
